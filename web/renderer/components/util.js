import * as CONSTANT from './constant';
import {EOL} from 'os';

export function parseSgf(sgfData){
    sgfData=sgfData.split(EOL);
    let data='';
    for(let i=0;i<sgfData.length;i++)
        data+=sgfData[i];
    let arr=data.split(';');
    let res=[];
    for(let i=0;i<arr.length;i++){
        let step=arr[i];
        let color=CONSTANT.COLOR_DICT[step[0]];
        let x=CONSTANT.ALPHABET[step[2]];
        let y=CONSTANT.ALPHABET[step[3]];
        if(x&&y&&color){
            res.push({
                "color":color,
                "x":x,
                "y":y
            });
        }
    }
    return res;
}

